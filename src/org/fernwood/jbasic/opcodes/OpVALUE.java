package org.fernwood.jbasic.opcodes;

import org.fernwood.jbasic.Status;
import org.fernwood.jbasic.runtime.JBasicException;
import org.fernwood.jbasic.value.Value;

/**
 * @author cole
 * 
 */
public class OpVALUE extends AbstractOpcode {

	/**
	 * Create a new value on the stack of the given type, initialized to a
	 * suitable default value.
	 * 
	 * @see org.fernwood.jbasic.opcodes.AbstractOpcode#execute(org.fernwood.jbasic.opcodes.InstructionContext)
	 */
	public void execute(final InstructionContext env) throws JBasicException {

		final int mode = env.instruction.integerOperand;
		if( Value.typeToName(mode).equals("UNDEFINED"))
			throw new JBasicException(Status.FAULT,
					new Status(Status.INVOPARG, mode));
		
		env.push(new Value(mode, null));

	}

}
